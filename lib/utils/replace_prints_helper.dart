// This file contains helper documentation for replacing print statements
// 
// To replace print statements throughout the codebase:
// 1. Import logging_service.dart: import '../services/logging_service.dart';
// 2. Replace print() with logger.debug(), logger.info(), logger.warning(), or logger.error()
// 3. Replace debugPrint() with logger.debug()
//
// Examples:
// - print('Debug message') -> logger.debug('Debug message')
// - print('Info message') -> logger.info('Info message')
// - print('Error: $e') -> logger.error('Error occurred', error: e)
// - debugPrint('Debug') -> logger.debug('Debug')



