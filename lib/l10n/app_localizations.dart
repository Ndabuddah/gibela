/// App localization class
class AppLocalizations {
  final Locale locale;
  
  AppLocalizations(this.locale);
  
  static AppLocalizations? of(BuildContext context) {
    return Localizations.of<AppLocalizations>(context, AppLocalizations);
  }
  
  static const LocalizationsDelegate<AppLocalizations> delegate = _AppLocalizationsDelegate();
  
  // Supported locales
  static const List<Locale> supportedLocales = [
    Locale('en', ''),
    Locale('zu', ''),
    Locale('xh', ''),
    Locale('af', ''),
    Locale('st', ''),
  ];
  
  // Translation maps
  static final Map<String, Map<String, String>> _localizedValues = {
    'en': _en,
    'zu': _zu,
    'xh': _xh,
    'af': _af,
    'st': _st,
  };
  
  String translate(String key) {
    return _localizedValues[locale.languageCode]?[key] ?? _en[key] ?? key;
  }
  
  // Getters for common strings
  String get appName => translate('app_name');
  String get bookRide => translate('book_ride');
  String get findDriver => translate('find_driver');
  String get myRides => translate('my_rides');
